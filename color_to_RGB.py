
def transform(color):
    if color == "red":
        return (255, 0, 0)
    elif color == "green":
        return (0, 255, 0)
    elif color == "blue":
        return (0, 0, 255)
    elif color == "orange":
        return (255, 165, 0)
    elif color == "white":
        return (255, 255, 255)
    elif color == "black":
        return (0, 0, 0)
    else:
        return (0, 0, 0)